//function declaration
function soma(x, y){
    return x + y;
}


//function Expression
const sub = function(x,y){
return x - y;
}
 
// named Function expression

const mult = function mult(x,y){
return x*y;
}


console.log("Soma: " +soma(3,4));
console.log("Sub: " +sub(3,4));
console.log("Multiplicação: " +mult(3,4));






